    private void agregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_agregarActionPerformed
        
        String tiendaDestino =textoUsuario1.getText();
        int tiempo = Integer.parseInt(textoUsuario2.getText());
        if (tiendaDestino.equals("") || tiempo==0){
            JOptionPane.showMessageDialog(null, "Ingrese todos los Campos Obligatorios");
        }else{
            Tiempo nuevo = new Tiempo(codigoTienda, tiendaDestino, tiempo);
            accesoAObjetos.AccesoTiempo llenar = new AccesoTiempo();
            
             if(!llenar.insertarTiempo(nuevo))
        {   JOptionPane.showMessageDialog(null, "No se puedo Ingresar un Nuevo Tiempo en la Tienda con el Codigo: "+codigoTienda+"\ndebido a que tiene datos repetidos con otra Tiempo ingresada en la Base de Datos");
        }else{
            JOptionPane.showMessageDialog(null, "Tiempo entre Tiendas Agregado Exitosamente");
            MenuEmpleado eleccion = new MenuEmpleado(nombreTienda,codigoTienda);
              eleccion.setVisible(true);
              this.setVisible(false);
              this.dispose();
        }
        }
        
    }//GEN-LAST:event_agregarActionPerformed

