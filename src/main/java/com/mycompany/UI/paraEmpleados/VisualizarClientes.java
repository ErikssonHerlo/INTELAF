    private void buscarTiendaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buscarTiendaActionPerformed
       
        if(comboNombreCliente.getSelectedIndex()!=0 && comboNITCliente.getSelectedIndex()==0){
            mostrarTablaBusqueda1(listaNombreClientes.get(comboNombreCliente.getSelectedIndex()-1));          
            comboNombreCliente.setSelectedIndex(0);
        } else if(comboNombreCliente.getSelectedIndex()==0 && comboNITCliente.getSelectedIndex()!=0 ){
            mostrarTablaBusqueda2(listaNITClientes.get(comboNITCliente.getSelectedIndex()-1));          
            comboNITCliente.setSelectedIndex(0);
        } else if(comboNombreCliente.getSelectedIndex()==0 && comboNITCliente.getSelectedIndex()==0){
           JOptionPane.showMessageDialog(null, "Por favor, Seleccione un Campo de Busqueda");
        }
            else{
            JOptionPane.showMessageDialog(null, "Por favor, Seleccione Un Solo Campo de Busqueda");
          comboNombreCliente.setSelectedIndex(0);
            comboNITCliente.setSelectedIndex(0);  
        }
    }//GEN-LAST:event_buscarTiendaActionPerformed

