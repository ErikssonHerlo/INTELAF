    private void mostrarTablaCompleta() {
        DefaultTableModel model = new DefaultTableModel();
        Conexion conexion = new Conexion();
        ResultSet rs = conexion.getTabla("SELECT * FROM CLIENTE ORDER BY NIT ASC");
         model.setColumnIdentifiers(new Object[]{"Nombre", "NIT", "Telefono", "Credito Compra","DPI","E-Mail","Direccion"});
        
        try {
            while (rs.next()) {               
                model.addRow(new Object[]{rs.getString("Nombre"), rs.getString("NIT"), rs.getString("Telefono"), rs.getDouble("Credito_Compra"),rs.getString("DPI"),rs.getString("Correo_Electronico"),rs.getString("Direccion")});
            }
            tableClientes.setModel(model);
        } catch (SQLException e) {
        }
    }
