    private void llenarCombos() {
        
        Conexion conexion = new Conexion();
        ResultSet rs = conexion.getTabla("SELECT Nombre FROM CLIENTE");
        try {
            while (rs.next()) {
                comboNombreCliente.addItem(rs.getString(1));
                listaNombreClientes.add(rs.getString(1));
                }

        } catch (SQLException e) {

        }
        ResultSet rs1 = conexion.getTabla("SELECT NIT FROM CLIENTE");
       
        try {
            while (rs1.next()) {
                comboNITCliente.addItem(rs1.getString(1));
                listaNITClientes.add(rs1.getString(1));
                }

        } catch (SQLException e) {

        }
         
    }
